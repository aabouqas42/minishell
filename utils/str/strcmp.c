#include "../../include/minishell.h"

int	_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while ((s1 && s2) && (s1[i] || s2[i]))
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}
